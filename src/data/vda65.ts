function createVda65Item({
  id,
  number,
  section,
  requirement,
  defectClass,
  specialCharacteristic = '',
  unit = '',
  minTolerance = '',
  nominalValue = '',
  maxTolerance = '',
  sampleSize = null,
  photoReference = '',
}: {
  id: string
  number: string
  section: string
  requirement: string
  defectClass: Vda65ChecklistItem['defectClass']
  specialCharacteristic?: string
  unit?: string
  minTolerance?: string
  nominalValue?: string
  maxTolerance?: string
  sampleSize?: number | null
  photoReference?: string
}): Vda65ChecklistItem {
  return {
    id,
    number,
    section,
    requirement,
    status: 'OK',
    specialCharacteristic,
    defectClass,
    unit,
    minTolerance,
    nominalValue,
    maxTolerance,
    sampleSize,
    defectCount: 0,
    photoReference,
    comment: '',
  }
}

